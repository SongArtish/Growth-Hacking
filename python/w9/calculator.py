def addition(n1, n2):
    print(f'덧셈 연산 결과: {n1 + n2}')

def substraction(n1, n2):
    print(f'뺄셈 연산 결과: {n1 - n2}')

def multiplition(n1, n2):
    print(f'곱셈 연산 결과: {n1 * n2}')

def division(n1, n2):
    print(f'나눗셈 연산 결과: {n1 / n2}')

def rest(n1, n2):
    print(f'나머지 연산 결과: {n1 % n2}')

def portion(n1, n2):
    print(f'몫 연산 결과: {n1 + n2}')